package au.edu.sydney.comp5216.assignment2;

import android.content.Context;
import android.graphics.drawable.Drawable;
import android.view.View;
import android.view.ViewGroup;
import android.widget.BaseAdapter;
import android.widget.GridView;
import android.widget.ImageView;

import java.util.List;

/**
 * Image adapter for gridView
 */
public class ImageAdapter extends BaseAdapter {
    //Activity
    private Context mContext;

    //Set of images will display in gallery
    private List<Drawable> mPictures;

    public ImageAdapter(Context c, List<Drawable> list) {
        mContext = c;
        mPictures = list;
    }

    @Override
    public int getCount() {
        return mPictures.size();
    }

    @Override
    public Object getItem(int i) {
        return null;
    }

    @Override
    public long getItemId(int i) {
        return 0;
    }

    /**
     * Set image view in each grid
     * @param i position
     * @param view one single grid
     * @param viewGroup who grid view
     * @return entire grid view
     */
    @Override
    public View getView(int i, View view, ViewGroup viewGroup) {
        ImageView imageView;
        if (view == null) {
            imageView = new ImageView(mContext);
            imageView.setLayoutParams(new GridView.LayoutParams(200, 150));
            imageView.setScaleType(ImageView.ScaleType.CENTER_CROP);
            imageView.setPadding(8, 8, 8, 8);
        } else {
            imageView = (ImageView) view;
        }
        imageView.setImageDrawable(mPictures.get(i));
        return imageView;
    }

}
